            <h2 id="title-6">
                La sécurité de vos données personnelles 
            </h2>

            <p class="p-content">
            Miliboo a mis en place des mesures techniques et organisationnelles afin de protéger vos données personnelles, notamment contre d’éventuels évènements susceptibles d’entraîner, de manière accidentelle ou volontaire, la destruction, la perte, l’altération, ou la diffusion non autorisée de vos données personnelles.  

            Consciente de la responsabilité qui lui incombe, Miliboo se porte responsable en cas de fuite de données. Ainsi, en cas d’incident, Miliboo avertira ses utilisateurs dans les plus brefs délais, et fournira un rapport d’analyse de l’incident complet sous 72h, suivant les consignes de la CNIL. 

            Il est aussi du devoir de Miliboo de permettre à l’utilisateur d’assurer la sécurité de son compte. Ainsi, le Site exige, dans le cadre de la création d’un compte client, la saisie d’un mot de passe suffisamment robuste. Ces critères proviennent des recommandations de la CNIL (12 caractères, 1 majuscule, 1 nombre, 1 caractère spécial). 

            Chaque titulaire de compte est responsable de la confidentialité des informations saisies. Le mot de passe ne doit pas être divulgué. Le Site et son service client ne vous demanderont jamais de le partager. De même, vos informations bancaires, votre adresse ou coordonnées doivent rester privées. 

            Miliboo s’engage à la protection de vos données sensibles par l’utilisation d’algorithmes de cryptage efficaces et à un accès strictement réglementé à vos données personnelles.  

            Miliboo a mis en place le système dit de l’« Opt-in » (toute option demandant votre consentement est désactivée par défaut) afin de garantir le niveau de vie privée maximum pour les utilisateurs du site. Miliboo fait aussi le choix de prendre en considération la protection de la vie privée des utilisateurs par défaut (concept du « Privacy by default »), en choisissant de ne collecter que les données strictement nécessaires, et en choisissant de concevoir le Site de la manière la plus sécurisée possible (concept du « Privacy by design ») lorsque le choix se présente. 
            </p>
